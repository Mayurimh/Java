package Abstraction;

public class CalculatorImp extends Calculator {
	public int addition(int a, int b) {
		return a+b;
	}
	public int subtraction(int a, int b) {
		return a-b;
	}
	public int multiplication(int a,int b) {
		return a*b;
	}
	public int modulas(int a, int b) {
		return a%b;
	}
}
